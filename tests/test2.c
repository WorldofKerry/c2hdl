int main() {
    int i = 0;
    i++;
    printf("i = %d\n", i);
    i--;
    printf("i = %d\n", i);
    i += 1;
    printf("i = %d\n", i);
    i -= 1;
    printf("i = %d\n", i);
    i *= 1;
    printf("i = %d\n", i);
    i /= 1;
    printf("i = %d\n", i);
    i %= 1;
    printf("i = %d\n", i);
    return 0;
}